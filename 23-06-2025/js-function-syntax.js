let add = (num1, num2)=>{
    return num1 + num2;
}

let subtract = (num1, num2)=>{
    return num1 - num2;
}

let multiply = (num1, num2)=>{
    return num1 * num2;
}

let division = (num1, num2)=>{
    return num1 / num2;
}

console.log(add(5, 40));
console.log(subtract(5, 40));
console.log(multiply(5, 40));
console.log(division(500, 40));
